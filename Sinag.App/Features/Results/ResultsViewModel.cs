using CommunityToolkit.Mvvm.ComponentModel;
using CommunityToolkit.Mvvm.Input;
using Sinag.Shared.Contracts;

namespace Sinag.App.Features.Results;

[QueryProperty(nameof(EstimateResponse), "EstimateResponse")]
[QueryProperty(nameof(EstimateRequest), "EstimateRequest")]
public partial class ResultsViewModel : ObservableObject
{
    [ObservableProperty]
    private EstimateResponse? _estimateResponse;

    [ObservableProperty]
    private EstimateRequest? _estimateRequest;

    [ObservableProperty]
    private int _selectedTierIndex;

    [ObservableProperty]
    private BomTier? _selectedTier;

    [ObservableProperty]
    private string _selectedTierName = "Budget";

    public ResultsViewModel()
    {
    }

    partial void OnEstimateResponseChanged(EstimateResponse? value)
    {
        if (value != null)
        {
            UpdateSelectedTier();
        }
    }

    partial void OnSelectedTierIndexChanged(int value)
    {
        UpdateSelectedTier();
    }

    private void UpdateSelectedTier()
    {
        if (EstimateResponse == null) return;

        (SelectedTier, SelectedTierName) = SelectedTierIndex switch
        {
            0 => (EstimateResponse.Bom.Budget, "Budget"),
            1 => (EstimateResponse.Bom.MidRange, "Mid-Range"),
            2 => (EstimateResponse.Bom.Premium, "Premium"),
            _ => (EstimateResponse.Bom.Budget, "Budget")
        };
    }

    [RelayCommand]
    private void SelectBudget()
    {
        SelectedTierIndex = 0;
    }

    [RelayCommand]
    private void SelectMidRange()
    {
        SelectedTierIndex = 1;
    }

    [RelayCommand]
    private void SelectPremium()
    {
        SelectedTierIndex = 2;
    }

    [RelayCommand]
    private async Task Share()
    {
        if (EstimateResponse == null || EstimateRequest == null) return;

        var text = $"Sinag Solar Estimate\n" +
                   $"Sistema: {EstimateResponse.SystemSizeKwp:F1} kWp\n" +
                   $"Konsumo: {EstimateRequest.KwhConsumed} kWh/buwan\n" +
                   $"Buwanang Savings: ₱{EstimateResponse.Financial.MonthlySavings:N2}\n" +
                   $"Payback Period: {EstimateResponse.Financial.PaybackPeriodMonths} na buwan\n" +
                   $"25-Year Savings: ₱{EstimateResponse.Financial.TwentyFiveYearSavings:N2}\n\n" +
                   $"Estimated Cost ({SelectedTierName}): ₱{SelectedTier?.TotalEstimate.Min:N0} - ₱{SelectedTier?.TotalEstimate.Max:N0}\n\n" +
                   $"Generated by Sinag App";

        await Microsoft.Maui.ApplicationModel.DataTransfer.Share.Default.RequestAsync(new ShareTextRequest
        {
            Text = text,
            Title = "Sinag Solar Estimate"
        });
    }

    [RelayCommand]
    private async Task NextSteps()
    {
        await Shell.Current.GoToAsync("NextStepsPage");
    }
}
